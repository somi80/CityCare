<h4>About the Project</h4>
<p>
CityCare is a Complaint Management System designed to improve cleanliness and civic issue reporting.
It allows citizens to report problems like garbage, drainage, and public hygiene.
</p>

<h4>Purpose</h4>
<p>
The goal of this system is to connect citizens with authorities and ensure quick resolution of complaints.
</p>

<h4>How it Works</h4>
<ul>
<li>User submits complaint</li>
<li>Admin assigns worker</li>
<li>Worker resolves issue</li>
<li>User tracks status</li>
</ul>

<h4>Key Features</h4>
<ul>
<li>Online complaint submission</li>
<li>Status tracking</li>
<li>Worker assignment</li>
<li>Vacancy and application system</li>
</ul>

